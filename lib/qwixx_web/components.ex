  embed_templates "components/*.html"

  defdelegate button(assigns), to: QwixxWeb.Component.Button
  defdelegate label(assigns), to: QwixxWeb.Component.Label
  defdelegate input(assigns), to: QwixxWeb.Component.Input

  attr :player_name, :string, required: true
